void byte_to_int(uint8_t *in, int *out, int size) {
   int i = 0;
   for(i=0; i<size; i++) {
      out[i] = (int) in[i];
   }
   return;
}

void int_to_byte(int *in, uint8_t *out, int size) {
   int i = 0;
   for(i=0; i<size; i++) {
      out[i] = (uint8_t) (in[i] & 0x0FF);
   }
   return;
}

